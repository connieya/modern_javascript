// 문자열 타입 => 불리언 타입
console.log(Boolean('x')); // true
console.log(Boolean('')); // false
console.log(Boolean('false')); // true

console.log("**************************")

// 숫자 타입 => 불리언 타입
console.log(Boolean(0)) // false
console.log(Boolean(1)) // true
console.log(Boolean(NaN)) // false
console.log(Boolean(Infinity)) // true
console.log(Boolean(-Infinity)) // true


console.log("@@@@@@@@@@@@@@@@@@@@@@@@@@@@@");
console.log(!!'x'); // true
console.log(!'x'); // false

